
using namespace std;

void summation(int a, int b){
    int sum=0;
    sum=a+b;
    cout<<"\t\t===>The summation of  "<<a<<" and "<<b<<" is "<<sum<<endl;
}
void subtraction(int a,int b){
    int sub;
    sub= a-b;
    cout<<"\t\t===>The subtraction of  "<<a<<" and "<<b<<" is "<<sub<<endl;
}
void Multiplication(float a, float b){
    float Mul=1;
    Mul=a*b;
    cout<<"\t\t===>The multiplication of  "<<a<<" and "<<b<<" is "<<Mul<<endl;
}
void Division(float a, float b){
    float Div;
    Div=a/b;
    cout<<"\t\t==>The division of  "<<a<<" and "<<b<<" is "<<Div<<endl;
}


